const DEFAULT_FILE_TYPE_ICON: FileTypeIconConfig = {
  mode: "stroke",
  paths: FILE_STROKE_PATHS,
  colorClass: "ftree-muted",
};

export interface FileTypeIconOptions {
  size?: number;
  className?: string;
}

export function createFileTypeIcon(
  filePath: string,
  options: FileTypeIconOptions = {},
): SVGSVGElement {
  const extension = filePath.split(".").pop()?.toLowerCase() ?? "";
  const icon = FILE_TYPE_ICONS[extension] ?? DEFAULT_FILE_TYPE_ICON;
  const size = options.size ?? 14;
  const iconNode: IconNode = icon.paths.map((d) => ["path", { d }] as const);

  const svg = createElement(iconNode, {
    viewBox: "0 0 24 24",
    width: size,
    height: size,
    "aria-hidden": "true",
  });

  const classNames = [icon.colorClass];
  if (options.className) classNames.push(options.className);
  svg.className.baseVal = classNames.join(" ");

  if (icon.mode === "fill") {
    svg.setAttribute("fill", "currentColor");
    svg.setAttribute("stroke", "none");
  } else {
    svg.setAttribute("fill", "none");
    svg.setAttribute("stroke", "currentColor");
    svg.setAttribute("stroke-width", "2");
    svg.setAttribute("stroke-linecap", "round");
    svg.setAttribute("stroke-linejoin", "round");
  }

  return svg;
}
